    private void botaoAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAdicionarProdutoActionPerformed
        Produto produto = new Produto();

        try {
            int cod = Integer.parseInt(fieldCodProduto.getText());
            produto = ServicoProduto.obterProduto(cod);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ItemVenda itemvenda = new ItemVenda();
        itemvenda.setItemProduto(produto);
        if (fieldQtdProduto.getText() == null || fieldQtdProduto.getText().equals("")) {
            itemvenda.setQtde(1);
            itemvenda.calcularPreco();
        } else {
            double qtd = Double.parseDouble(fieldQtdProduto.getText());
            itemvenda.setQtde(qtd);
            itemvenda.calcularPreco();
        }

        try {
            ListaItems.add(itemvenda);
        } catch (Exception e) {

        }

        try {
            resultSearch = refreshList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(),
                    "Falha ao adicionar item", JOptionPane.ERROR_MESSAGE);
            return;
        }

        totalVenda = totalVenda + itemvenda.getPreco();
        labelValorTotal.setText("" + totalVenda);


    }//GEN-LAST:event_botaoAdicionarProdutoActionPerformed

