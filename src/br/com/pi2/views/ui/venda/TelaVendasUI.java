    public boolean refreshList() throws VendasException, Exception {

        DefaultTableModel model = (DefaultTableModel) TabelaItemVenda.getModel();
        model.setRowCount(0);
        if (ListaItems == null || ListaItems.size() <= 0) {
            return false;
        }
        for (int i = 0; i < ListaItems.size(); i++) {
            ItemVenda item = ListaItems.get(i);
            if (item != null) {
                Object[] row = new Object[5];
                row[0] = item.getItemProduto().getCodigoProduto();
                row[1] = item.getItemProduto().getDescricao();
                row[2] = item.getItemProduto().getValor();
                row[3] = item.getQtde();
                row[4] = item.getPreco();
                model.addRow(row);
            }
        }

        return true;
    }

