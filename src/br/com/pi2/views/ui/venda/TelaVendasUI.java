    private void BotaoFinalizarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoFinalizarVendaActionPerformed
        venda.setItemVenda(ListaItems);
        venda.setValor(totalVenda);
        try {
            ServicoVenda.adicionarVenda(venda);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Não é possível "
                    + "concluir a venda ",
                    "erro ao finalizar", JOptionPane.ERROR_MESSAGE);
        }
        JOptionPane.showMessageDialog(rootPane, "Venda Concluida!");

    }//GEN-LAST:event_BotaoFinalizarVendaActionPerformed

